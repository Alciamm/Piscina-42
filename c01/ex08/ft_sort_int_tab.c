void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	aux;

	i = 0;
	j = 0;
	aux = 0;
	while (i < size - 1)
	{
		j = i + 1;
		while (j < size)
		{
			if (tab[i] > tab[j])
			{
			aux = tab[i];
			tab[i] = tab[j];
			tab[j] = aux;
			}
			j++;
		}
		i++;
	}
}
