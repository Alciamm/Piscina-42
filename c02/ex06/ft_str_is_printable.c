int	ft_str_is_printable(char *str)
{
	int	x;

	x = 0;
	while (str[x] != '\0')
	{
		if (str[x] >= 32 && str[x] <= 126)
		{
			x++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
