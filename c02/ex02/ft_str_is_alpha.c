int	ft_str_is_alpha(char *str)
{
	int	x;

	x = 0;
	while ((str[x] >= 'a' && str[x] <= 'z') || (str[x] >= 'A' && str[x] <= 'Z'))
	{
		x++;
	}
	while (str[x] == '\0')
	{
		return (1);
	}
	while (str[x] != '\0')
	{
		return (0);
	}
	return (1);
}
